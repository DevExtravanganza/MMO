    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("INFDEV_MMORPG_2.0PU");
        EntityManager em = emf.createEntityManager();
        currentUser = em.find(Users.class, currentUser.getUserName());
        int userBalance = currentUser.getBalance() - 1;
        Integer iUserBalance = (Integer) userBalance;
        String userBalanceString = iUserBalance.toString();
        int newNrOfSlots = currentUser.getCharacterSlots() + 1;
        if (currentUser.getBalance() < 1) {
            jLabel10.setText("Your balance must be above 1.");
        } else {
            em.getTransaction().begin();
            
            Query q = em.createQuery("UPDATE Users SET balance = " + userBalance + ", characterSlots = "+ newNrOfSlots +" WHERE userName ='" + currentUser.getUserName() + "'");
            q.executeUpdate();
            em.getTransaction().commit();
            Integer iSlots = (Integer) newNrOfSlots;
            String slotString = iSlots.toString();
            jLabel2.setText(userBalanceString);
            jLabel14.setText(slotString);
            jLabel15.setText("One character slot was added to your account.");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

