    private void subscriptionHandler(int subPeriod, int price) {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("INFDEV_MMORPG_2.0PU");
        EntityManager em = emf.createEntityManager();
        currentUser = em.find(Users.class, currentUser.getUserName());
        int userBalance = currentUser.getBalance() - price;
        Integer iUserBalance = (Integer) userBalance;
        String userBalanceString = iUserBalance.toString();
        int userMonth = currentUser.getMonthsPaid() + subPeriod;
        if (currentUser.getBalance() < price) {
            jLabel10.setText("Your balance must be above " + price + ".");
        } else {
            em.getTransaction().begin();
            SimpleDateFormat df = new SimpleDateFormat("yyyy/MM/dd");
            Date dateobj = new Date();
            String today = df.format(dateobj);
            Query q = em.createQuery("UPDATE Users SET balance = " + userBalance + ", monthsPaid = " + userMonth + ", lastPayment = '"+ today +"' WHERE userName ='" + currentUser.getUserName() + "'");
            q.executeUpdate();
            em.getTransaction().commit();
            Integer iMonth = (Integer) userMonth;
            String monthString = iMonth.toString();
            jLabel4.setText(monthString);
            jLabel2.setText(userBalanceString);
            jLabel10.setText(subPeriod + " month(s) was/were added to your account.");
        }
    }

